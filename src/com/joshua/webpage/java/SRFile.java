public class SRFile {
    private String type;
    private String id;
    private String srpId;
    private String srxId;
    private String base;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSrpId() {
        return srpId;
    }

    public void setSrpId(String srpId) {
        this.srpId = srpId;
    }

    public String getSrxId() {
        return srxId;
    }

    public void setSrxId(String srxId) {
        this.srxId = srxId;
    }

    public String getBase() {
        return base;
    }

    public void setBase(String base) {
        this.base = base;
    }
}
